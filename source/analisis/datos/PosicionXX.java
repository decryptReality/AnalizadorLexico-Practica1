package analisis.datos;

// - posiciones en el eje X
// - guardar lugares a resaltar en un JTextArea
public class PosicionXX 
{
    public PosicionXX(int x1, int x2) {
        this.x1 = x1;
        this.x2 = x2;
    }

    private int x1;
    private int x2;
    
    public int getX1() {
        return x1;
    }
    public int getX2() {
        return x2;
    }
}
